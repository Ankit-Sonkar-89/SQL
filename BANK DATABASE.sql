CREATE DATABASE BANK_DATABASE ;
USE BANK_DATABASE;
#PERSONAL INFO

CREATE TABLE CUSTOMER_PERSONAL_INFO ( 
CUSTOMER_ID VARCHAR (50) ,
CUSTOMER_NAME VARCHAR (50) ,
DOB DATE ,
GUARDIAN_NAME VARCHAR (30) ,
ADRESS VARCHAR (50) ,
CONTACT_NO BIGINT(10) ,
MAIL_ID VARCHAR (30) ,
GENDER CHAR (1) ,
MARITAL_STATUS VARCHAR(10) ,
IDENTIFICATION_DO_TYPE VARCHAR (20) ,
IDENTIFICATION_DOC_NO VARCHAR (20),
CITIZENSHIP VARCHAR(20) ,
CONSTRAINT CUSTOMER_PERSONAL_INFO_PK PRIMARY KEY ( CUSTOMER_ID )
);
# CUSTOMER REFERENCE INFO

CREATE TABLE CUSTOMER_REFERENCE_INFO (
 CUSTOMER_ID VARCHAR (5) ,
 REFERNCE_ACCOUNT_NAME VARCHAR(30) ,
 REFERENCE_ACCOUNT_NO BIGINT(16) ,
 REFERNCE_ACCOUNT_ADDRESS VARCHAR(50) ,
 RELATION VARCHAR (25) ,
 CONSTRAINT CUSTOMER_REFERENCE_INFO_PK PRIMARY KEY ( CUSTOMER_ID ) ,
 CONSTRAINT CUSTOMER_REFERENCE_INFO_FK FOREIGN KEY ( CUSTOMER_ID )  REFERENCES CUSTOMER_PERSONAL_INFO ( CUSTOMER_ID ) 
 );
 
 # BANK INFO 
 
 CREATE TABLE BANK_INFO (
 IFSC_CODE VARCHAR(15) , 
 BANK_NAME VARCHAR(30) ,
 BRANCH_NAME VARCHAR(30) ,
 CONSTRAINT BANK_INFO_PK PRIMARY KEY ( IFSC_CODE )
 );
 
 # ACCOUNT INFO 
 
 CREATE TABLE ACCOUNT_INFO (
 ACCOUNT_NO BIGINT(16),
 CUSTOMER_ID VARCHAR(5) ,
 ACCOUNT_TYPE VARCHAR(100) ,
 REGESTRATION_DATE DATE,
 ACTIVATION_DATE DATE,
 IFSC_CODE VARCHAR(10),
 INTEREST DECIMAL(7,2),
 INTEREST_DEPOSIT BIGINT(10),
 CONSTRAINT ACCOUNT_INFO_PK PRIMARY KEY ( ACCOUNT_NO ) ,
 CONSTRAINT ACCOUNT_INFO_PERSONAL_FK FOREIGN KEY ( CUSTOMER_ID ) REFERENCES CUSTOMER_PERSONAL_INFO ( CUSTOMER_ID ) ,
 CONSTRAINT ACCOUNT_INFO_BANK_FK FOREIGN KEY ( IFSC_CODE ) REFERENCES BANK_INFO ( IFSC_CODE )
 );
 
